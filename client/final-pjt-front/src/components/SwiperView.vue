<template>
  
  <div>
    <swiper ref="filterSwiper" :options="swiperOption" role="tablist">
    <swiper-slide role="tab" style="height:700px;"
    v-for="movie in popmovies"
    :key="movie.id">
      <img :src="movie.img_url" alt="">
    </swiper-slide>  
    </swiper>
  </div>
</template>

<script>
import 'swiper/dist/css/swiper.css'
import { swiper, swiperSlide } from 'vue-awesome-swiper'


export default {
  name: 'SwiperView',
  components: {
    swiper,
    swiperSlide
  },
  data () {
    return {
      swiperOption: {
        slidesPerView: 'auto',
        spaceBetween: 6, // swiper-slide 사이의 간격 지정
        slidesOffsetBefore: 0, // slidesOffsetBefore는 첫번째 슬라이드의 시작점에 대한 변경할 때 사용
        slidesOffsetAfter: 0, // slidesOffsetAfter는 마지막 슬라이드 시작점 + 마지막 슬라이드 너비에 해당하는 위치의 변경이 필요할 때 사용
        freeMode: true, // freeMode를 사용시 스크롤하는 느낌으로 구현 가능
        centerInsufficientSlides: true, // 컨텐츠의 수량에 따라 중앙정렬 여부를 결정함
      },
      popmovies : null
    }
  },
  computed: {
    savedMovies() {
      return this.$store.state.moviesStore.savedMovies
    }
  },
  created() {
    this.popMovies()
  },
  methods: {
    popMovies() {
      this.popmovies = this.savedMovies.slice(0,20)
    }
  },
}
</script>

